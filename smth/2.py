def swap_with_xor(a, b):
    """Обмен значений с использованием XOR"""
    print(f"Исходные значения: a = {a}, b = {b}")

    a = a ^ b
    print(f"После a = a ^ b: a = {a}, b = {b}")

    b = a ^ b
    print(f"После b = a ^ b: a = {a}, b = {b}")

    a = a ^ b
    print(f"После a = a ^ b: a = {a}, b = {b}")

    return a, b


# Пример:
print("Пример обмена значений с XOR:")
a, b = 5, 3
result = swap_with_xor(a, b)
print(f"Результат: {result}\n")

